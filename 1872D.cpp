void Solve(){
    ll n, x, y; cin >> n >> x >> y;
    ll lcm = x * y / __gcd(x, y);
    ll a = n / x, b = n / y, c = n / lcm;
    a -= c, b -= c;
    ll res = ((2 * n - a + 1) * a / 2 - (1 + b) * b / 2);
    cout << res << endl;
    return;
}
