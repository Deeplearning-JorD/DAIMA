void Solve(){
    string s; cin >> s;
    s = s + '0';
    ll sum = 0;
    for(auto x:s){
        if(x == '0'){
            cout << (char)('a' + sum - 1);
            sum = 0;
            continue;
        }
        sum += x - '0';
    }
    cout << endl;
    return;
}
