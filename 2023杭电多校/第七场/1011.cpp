void Solve(){
    ll x, a, b;
    cin >> x >> a >> b;
    ll res = 0;
    while(x){
        if(min((x + a)/2, (ll)sqrt(x + b)) <= x - 1){
            res ++;
            x = min((x + a)/2, (ll)sqrt(x + b));
        }else{
            res += x;
            x = 0;
        }
    }
    cout << res << endl;
    return;
}
