void Solve(){
    int n;
    cin >> n;
    string s;
    cin >> s;
    int cnt = 0;
    for(auto x:s){
        if(x == 'A'){
            cnt --;
            cnt = max(0, cnt);
        }else cnt ++;
    }
    if(cnt) cout << "No\n";
    else cout << "Yes\n";
    return;
}
