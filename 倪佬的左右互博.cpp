void Solve(){
    int n, m, t;
    cin >> n >> m >> t;
    int mi = 1e9;
    while(t --){
        int x, y;
        cin >> x >> y;
        mi = min({mi, x - 1, y - 1, n - x, m - y});
    }
    if(mi <= 4) cout << "Right one is smarter\n";
    else cout << "Left one is smarter\n";
    return;
}
