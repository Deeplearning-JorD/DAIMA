void Solve(){
    int n, k;
    cin >> n >> k;
    k = min(n, k);
    for(int i = 1;i <= n / i;i ++){
        if(n % i == 0){
            int a = i, b = n / i;
            if(a > 1 && a <= k){
                cout << "NO\n";
                return;
            }
            swap(a, b);
            if(a > 1 && a <= k){
                cout << "NO\n";
                return;
            }
        }
    }
    cout << "YES\n";
    return;
}
