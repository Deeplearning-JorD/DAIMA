void Solve(){
    int n, k;
    cin >> n >> k;
    int now = 1;
    int last = 0;
    for(int i = 1;i <= n;i ++){
        if(k - (last + now) < n - i){
            last ++;
        }else{
            last += now;
            now ++;
        }
        cout << last <<  ' ';
    }
    cout << endl;
    return;
}
