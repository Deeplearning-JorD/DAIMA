void Solve(){
    int n, m;
    cin >> n >> m;
    vector<int> st(n + 1);
    while(m --){
        int op, x;
        cin >> op >> x;
        if(op == 1){
            st[x] += 1;
        }else if(op == 2){
            st[x] += 2;
        }else cout << ((st[x] >= 2)?"Yes\n":"No\n");
    }
    return;
}
