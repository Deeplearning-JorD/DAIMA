void Solve(){
    ll n; cin >> n;
    ll l, r;
    for(ll i = 1;i <= n / i;i ++){
        if(n % i) continue;
        if(i * (n / i) / __gcd(i, n / i) != n) continue;
        l = i, r = n / i;
    }
    cout << l << ' ' << r << endl;
    return;
}
